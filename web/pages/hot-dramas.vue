  if (subType) {
    return dramas.value.filter(drama => drama.category === category && drama.sub_type === subType)
  }
  return dramas.value
})

const movieCount = computed(() => {
  return dramas.value.filter(drama => drama.category === 'ÁîµÂΩ±').length
